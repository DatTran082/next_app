import { MediaPipeFaceMeshTfjsModelConfig } from '@tensorflow-models/face-landmarks-detection'

function LandMark() {
	const webcamRef = useRef<any>(null)
	const canvasRef = useRef<any>(null)

	useEffect(() => {
		const runFacemesh = async () => {
			const model = faceLandmarksDetection.SupportedModels.MediaPipeFaceMesh
			const detectorConfig: MediaPipeFaceMeshTfjsModelConfig = {
				runtime: 'tfjs',
				refineLandmarks: true,
			}
			const detector = await faceLandmarksDetection.createDetector(model, detectorConfig)

			setInterval(() => {
				detect(detector)
			}, 10)
		}
		runFacemesh()
	}, [])

	const detect = async (model: FaceDetector) => {
		if (typeof webcamRef.current !== 'undefined' && webcamRef.current !== null && webcamRef.current.video.readyState === 4) {
			// Get Video Properties
			const video = webcamRef.current.video
			const videoWidth = webcamRef.current.video.videoWidth
			const videoHeight = webcamRef.current.video.videoHeight

			// Set video width
			webcamRef.current.video.width = videoWidth
			webcamRef.current.video.height = videoHeight

			// Set canvas width
			canvasRef.current.width = videoWidth
			canvasRef.current.height = videoHeight

			// Make Detections
			// OLD MODEL
			//       const face = await net.estimateFaces(video);
			// NEW MODEL
			const face = await model.estimateFaces(video, { flipHorizontal: false })
			console.log(face)

			// Get canvas context
			const ctx = canvasRef.current.getContext('2d')

			ctx.drawImage(video, 0, 0, video.videoWidth, video.videoHeight)
			ctx.translate(video.videoWidth, 0)
			// ctx.scale(-1, 1)

			requestAnimationFrame(() => {
				drawMesh(ctx, face, true, true)
			})
		}
	}

	return (
		<div className="App">
			<header className="App-header">
				<Webcam
					ref={webcamRef}
					style={{
						position: 'absolute',
						marginLeft: 'auto',
						marginRight: 'auto',
						left: 0,
						right: 0,
						textAlign: 'center',
						zIndex: 9,
						width: 640,
						height: 480,
					}}
				/>

				<canvas
					ref={canvasRef}
					style={{
						position: 'absolute',
						marginLeft: 'auto',
						marginRight: 'auto',
						left: 0,
						right: 0,
						textAlign: 'center',
						zIndex: 9,
						width: 640,
						height: 480,
					}}
				/>
			</header>
		</div>
	)
}

export default LandMark

